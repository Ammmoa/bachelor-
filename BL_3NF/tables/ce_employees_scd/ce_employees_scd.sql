CREATE TABLE IF NOT EXISTS BL_3NF.CE_EMPLOYEES_SCD (
EMPLOYEE_ID int, 
LASTNAME VARCHAR(30), 
FIRSTNAME VARCHAR(30), 
EMAIL VARCHAR(50), 
BADGE VARCHAR(10), 
START_DT DATE, 
END_DT DATE, 
IS_ACTIVE BOOL, 
TA_INSERT_DT date, 
TA_UPDATE_DT date, 
SOURCE_SYSTEM varchar(50), 
SOURCE_ENTITY varchar(50), 
SOURCE_ID varchar(50), 
